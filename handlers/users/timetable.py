def show(para):
    timetablepara = ["І пара", "II пара", "IІІ пара", "ІV пара", "V пара", "VІ пара"]
    timetabletime = ["08.30-09:50", "10.00-11.20,", "12.00-13.20", "13.30-14.50", "15.00-16.20", "16.30-17.50"]
    p = (f"{timetablepara[0]}||{timetabletime[0]}\n"
         f"{para[0]}"
         f"{timetablepara[1]}||{timetabletime[1]}\n"
         f"{para[1]}"
         f"{timetablepara[2]}||{timetabletime[2]}\n"
         f"{para[2]}"
         f"{timetablepara[3]}||{timetabletime[3]}\n"
         f"{para[3]}"
         f"{timetablepara[4]}||{timetabletime[4]}\n"
         f"{para[4]}"
         f"{timetablepara[5]}||{timetabletime[5]}\n"
         f"{para[5]}")
    return p


def show_glav(van, dua, three, four, faif):
    p = show(van) + show(dua) + show(three) + show(four) + show(faif)
    return p
